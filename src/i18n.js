const generatedTextKeys = new Map(
  Object.entries(translations.en)
    .filter(([key]) => key.startsWith("advice.") || key.startsWith("risk.") || key.startsWith("reason.") || key === "brief.empty")
    .map(([key, value]) => [value, key])
);

export function translate(key, language = "en", values = {}) {
  const dictionary = translations[language] || translations.en;
  const template = dictionary[key] || translations.en[key] || key;
  return interpolate(template, values);
}

export function translateGeneratedText(text, language = "en") {
  const key = generatedTextKeys.get(text);
  if (key) return translate(key, language);

  const dynamicReason = matchDynamicReason(text);
  if (dynamicReason) {
    return translate(dynamicReason.key, language, { value: dynamicReason.value });
  }

  return text;
}

export function translateList(items, language = "en") {
  return items.map((item) => translateGeneratedText(item, language));
}

function interpolate(template, values) {
  return template.replaceAll(/\{(\w+)\}/g, (_, key) => values[key] ?? "");
}

function matchDynamicReason(text) {
  const patterns = [
    [/^Up (\d+) units versus previous record\.$/, "reason.upPrevious"],
    [/^Down (\d+) units versus previous record\.$/, "reason.downPrevious"],
    [/^Up (\d+) units versus the same day last week\.$/, "reason.upWeek"],
    [/^Down (\d+) units versus the same day last week\.$/, "reason.downWeek"],
    [/^7-day average is (\d+) units\.$/, "reason.average7"],
    [/^(\d+) returning customers recorded today\.$/, "reason.returningCustomers"]
  ];

  for (const [pattern, key] of patterns) {
    const match = text?.match(pattern);
    if (match) return { key, value: match[1] };
  }
  return null;
}
